/**
 * Prepare mint transaction with metadata URI
 * Contract mint function: mint(address to, uint256 fid, string uri) payable
 */
export function prepareMintTransaction(
  recipientAddress: Address,
  fid: number,
  metadataIpfsUri: string
): {
  to: Address;
  data: `0x${string}`;
  value: bigint;
  chain: typeof base;
} {
  const data = encodeFunctionData({
    abi: WINTER_CHEER_ABI,
    functionName: 'mint',
    args: [recipientAddress, BigInt(fid), metadataIpfsUri],
  });

  return {
    to: WINTER_CHEER_CONTRACT_ADDRESS,
    data,
    value: MINT_PRICE,
    chain: base,
  };
}

/**
 * Generate next token ID based on FID
 * Contract automatically assigns token IDs, so this returns the FID for reference
 */
export function generateTokenId(fid: number): bigint {
  return BigInt(fid);
}

/**
 * Get total minted count from blockchain
 */
export async function getTotalMinted(publicClient: any): Promise<number> {
  try {
    const result = await publicClient.readContract({
      address: WINTER_CHEER_CONTRACT_ADDRESS,
      abi: WINTER_CHEER_ABI,
      functionName: 'totalMinted',
    });
    return Number(result);
  } catch (error) {
    console.error('Error fetching total minted from contract:', error);
    return 0;
  }
}

